#include <iostream>
using namespace std;

int main() {

    int score = 0;
    cout << score << endl; // score is 0

    score = 4 + 2;
    cout << score << endl;// it is now 6

    score = 4 - 2;
    cout << score << endl;// it is now 2

    score = 4 * 2;
    cout << score << endl;// it is now 8

    score = 4 / 2;
    cout << score << endl;// and now 2
    
    score = 5 % 2;
    cout << score << endl; // and now 1
    return 0;
  
}
